export function detectSystemLanguage(locale?: string | null): ResolvedLanguage {
  const source = (locale ?? navigator.languages?.[0] ?? navigator.language ?? 'en').toLowerCase()
  if (source.startsWith('ko')) return 'ko'
  if (source.startsWith('ja')) return 'ja'
  return 'en'
}

export function resolveLanguage(language: LanguagePreference): ResolvedLanguage {
  if (language === 'system') return detectSystemLanguage()
  return language
}

export function languageToLocale(language: ResolvedLanguage): string {
  switch (language) {
    case 'ko':
      return 'ko-KR'
    case 'ja':
      return 'ja-JP'
    default:
      return 'en-US'
  }
}

export function translate(language: ResolvedLanguage, key: string, params?: TranslationParams): string {
  const value = translations[language][key] ?? translations.en[key] ?? key
  return typeof value === 'function' ? value(params) : value
}
